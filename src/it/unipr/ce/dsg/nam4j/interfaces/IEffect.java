package it.unipr.ce.dsg.nam4j.interfaces;

public interface IEffect {

	/**
     * set the Effect name.
     *
     * @param name.
     */
	void setName(String name);
	
	/**
     * get the Effect name.
     *
     * @return the name of the Effect.
     */
	String getName();
	
	/**
     * set the Effect id.
     *
     * @param id.
     */
	void setId(String id);
	
	/**
     * get the Effect id.
     *
     * @return the id of the Effect.
     */
	String getId();
}
