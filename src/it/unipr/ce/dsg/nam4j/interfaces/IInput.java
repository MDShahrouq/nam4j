package it.unipr.ce.dsg.nam4j.interfaces;

public interface IInput {

	/**
     * set the Input name.
     *
     * @param name.
     */
	void setName(String name);
	
	/**
     * get the Input name.
     *
     * @return the name of the Input.
     */
	String getName();
	
	/**
     * set the Input id.
     *
     * @param id.
     */
	void setId(String id);
	
	/**
     * get the Input id.
     *
     * @return the id of the Input.
     */
	String getId();
}
