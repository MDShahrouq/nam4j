package it.unipr.ce.dsg.nam4j.interfaces;

public interface IOutput {

	/**
     * set the Output name.
     *
     * @param name.
     */
	void setName(String name);
	
	/**
     * get the Output name.
     *
     * @return the name of the Output.
     */
	String getName();
	
	/**
     * set the Output id.
     *
     * @param id.
     */
	void setId(String id);
	
	/**
     * get the Output id.
     *
     * @return the id of the Output.
     */
	String getId();
}
